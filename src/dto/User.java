package dto;

public class User {
	private String userID;
	private String password;
	private String name;
	private String gender;
	private String birth;
	private String phone;
	private String email;
	private String address;
	private String workplace;
	private String school;
	private Integer squat;
	private Integer deadlift;
	private Integer benchpress;
	
	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBirth() {
		return birth;
	}
	public void setBirth(String birth) {
		this.birth = birth;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getWorkplace() {
		return workplace;
	}
	public void setWorkplace(String workplace) {
		this.workplace = workplace;
	}
	public String getSchool() {
		return school;
	}
	public void setSchool(String school) {
		this.school = school;
	}
	public Integer getSquat() {
		return squat;
	}
	public void setSquat(Integer squat) {
		this.squat = squat;
	}
	public Integer getDeadlift() {
		return deadlift;
	}
	public void setDeadlift(Integer deadlift) {
		this.deadlift = deadlift;
	}
	public Integer getBenchpress() {
		return benchpress;
	}
	public void setBenchpress(Integer benchpress) {
		this.benchpress = benchpress;
	}
	
	
}
